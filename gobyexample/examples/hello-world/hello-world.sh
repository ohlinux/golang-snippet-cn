# 要运行这个程序，将这些代码放到 `hello-world.go` 中
# 并且使用 `go run` 命令。
$ go run hello-world.go
hello world

# 有时候我们想将我们的程序编译成二进制文件。我们
# 可以通过 `go build` 命来达到目的。
$ go build hello-world.go
$ ls
hello-world	hello-world.go

# 然后我们可以直接运行这个二进制文件。
$ ./hello-world
hello world

# 现在我们可以运行和并以基本的 Go 程序，让我们学习更多的
# 关于这门语言的知识吧。
